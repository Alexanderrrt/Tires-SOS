-- Internal storage used only by authenticated server routes with the service role.
-- No browser client receives the service key, and anon/authenticated access is revoked.

create table if not exists public.dashboard_clients (
  id uuid primary key default gen_random_uuid(),
  business_email text not null unique,
  business_name text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

